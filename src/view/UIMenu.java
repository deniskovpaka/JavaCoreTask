package view;

/**
 * The Menu interface represents
 * common menu items.
 *
 * @author deniskovpaka
 */
public interface UIMenu {
    /**
     * Shows common UI interface for clients.
     */
    void showUserInterfaceMenu();

    /**
     * Show sorting menu.
     */
    void showSortMenu();

    /**
     * Show calculation menu.
     */
    void showCalculationMenu();

    /**
     * Show finding menu.
     */
    void showFindMenu();
}
